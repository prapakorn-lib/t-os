def read_at_position(filename, position, size=None):
    """
    ‡∏≠‡πà‡∏≤‡∏ô‡∏Ç‡πâ‡∏≠‡∏°‡∏π‡∏•‡∏à‡∏≤‡∏Å‡∏ï‡∏≥‡πÅ‡∏´‡∏ô‡πà‡∏á‡∏ó‡∏µ‡πà‡∏Å‡∏≥‡∏´‡∏ô‡∏î
    
    Args:
        filename (str): ‡∏ä‡∏∑‡πà‡∏≠‡πÑ‡∏ü‡∏•‡πå
        position (int): ‡∏ï‡∏≥‡πÅ‡∏´‡∏ô‡πà‡∏á‡πÄ‡∏£‡∏¥‡πà‡∏°‡∏ï‡πâ‡∏ô (offset)
        size (int): ‡∏à‡∏≥‡∏ô‡∏ß‡∏ô bytes ‡∏ó‡∏µ‡πà‡∏≠‡πà‡∏≤‡∏ô (None = ‡∏≠‡πà‡∏≤‡∏ô‡∏ó‡∏±‡πâ‡∏á‡∏´‡∏°‡∏î)
    """
    try:
        with open(filename, 'r', encoding='utf-8') as f:
            # ‡πÉ‡∏ä‡πâ seek() ‡πÄ‡∏û‡∏∑‡πà‡∏≠‡∏Å‡∏£‡∏∞‡πÇ‡∏î‡∏î‡πÑ‡∏õ‡∏¢‡∏±‡∏á‡∏ï‡∏≥‡πÅ‡∏´‡∏ô‡πà‡∏á‡∏ó‡∏µ‡πà‡∏ï‡πâ‡∏≠‡∏á‡∏Å‡∏≤‡∏£
            f.seek(position)
            
            if size:
                data = f.read(size)
            else:
                data = f.read()
            
            print(f"üìç ‡∏ï‡∏≥‡πÅ‡∏´‡∏ô‡πà‡∏á {position}, ‡∏≠‡πà‡∏≤‡∏ô {len(data)} bytes:")
            print(f"   '{data}'")
            
            return data
            
    except Exception as e:
        print(f"‚ùå Error: {e}")
        return None


def read_lines_by_index(filename, line_numbers):
    """
    ‡∏≠‡πà‡∏≤‡∏ô‡∏ö‡∏£‡∏£‡∏ó‡∏±‡∏î‡∏ó‡∏µ‡πà‡∏£‡∏∞‡∏ö‡∏∏‡πÇ‡∏î‡∏¢‡∏ï‡∏£‡∏á (Random Access)
    
    Args:
        filename (str): ‡∏ä‡∏∑‡πà‡∏≠‡πÑ‡∏ü‡∏•‡πå
        line_numbers (list): ‡∏´‡∏°‡∏≤‡∏¢‡πÄ‡∏•‡∏Ç‡∏ö‡∏£‡∏£‡∏ó‡∏±‡∏î‡∏ó‡∏µ‡πà‡∏ï‡πâ‡∏≠‡∏á‡∏Å‡∏≤‡∏£‡∏≠‡πà‡∏≤‡∏ô
    """
    try:
        with open(filename, 'r', encoding='utf-8') as f:
            lines = f.readlines()
            
            print("üìñ ‡∏≠‡πà‡∏≤‡∏ô‡∏ö‡∏£‡∏£‡∏ó‡∏±‡∏î‡πÅ‡∏ö‡∏ö‡∏™‡∏∏‡πà‡∏°:")
            print("-" * 60)
            
            for line_num in line_numbers:
                if 0 < line_num <= len(lines):
                    print(f"‡∏ö‡∏£‡∏£‡∏ó‡∏±‡∏î‡∏ó‡∏µ‡πà {line_num}: {lines[line_num-1].rstrip()}")
                else:
                    print(f"‚ö†Ô∏è  ‡∏ö‡∏£‡∏£‡∏ó‡∏±‡∏î‡∏ó‡∏µ‡πà {line_num}: ‡πÑ‡∏°‡πà‡∏°‡∏µ‡∏≠‡∏¢‡∏π‡πà")
            
            print("-" * 60)
            
    except Exception as e:
        print(f"‚ùå Error: {e}")


def demonstrate_seek_tell(filename):
    """
    ‡πÅ‡∏™‡∏î‡∏á‡∏Å‡∏≤‡∏£‡πÉ‡∏ä‡πâ‡∏á‡∏≤‡∏ô seek() ‡πÅ‡∏•‡∏∞ tell()
    
    Args:
        filename (str): ‡∏ä‡∏∑‡πà‡∏≠‡πÑ‡∏ü‡∏•‡πå
    """
    print("\nüîç ‡∏Å‡∏≤‡∏£‡πÉ‡∏ä‡πâ‡∏á‡∏≤‡∏ô seek() ‡πÅ‡∏•‡∏∞ tell()")
    print("-" * 60)
    
    try:
        with open(filename, 'r', encoding='utf-8') as f:
            # tell() - ‡∏î‡∏π‡∏ï‡∏≥‡πÅ‡∏´‡∏ô‡πà‡∏á‡∏õ‡∏±‡∏à‡∏à‡∏∏‡∏ö‡∏±‡∏ô
            print(f"‡∏ï‡∏≥‡πÅ‡∏´‡∏ô‡πà‡∏á‡πÄ‡∏£‡∏¥‡πà‡∏°‡∏ï‡πâ‡∏ô: {f.tell()}")
            
            # ‡∏≠‡πà‡∏≤‡∏ô‡∏ö‡∏≤‡∏á‡∏™‡πà‡∏ß‡∏ô
            data = f.read(20)
            print(f"‡∏≠‡πà‡∏≤‡∏ô 20 bytes: '{data}'")
            print(f"‡∏ï‡∏≥‡πÅ‡∏´‡∏ô‡πà‡∏á‡∏õ‡∏±‡∏à‡∏à‡∏∏‡∏ö‡∏±‡∏ô: {f.tell()}")
            
            # seek(0) - ‡∏Å‡∏•‡∏±‡∏ö‡πÑ‡∏õ‡∏ï‡πâ‡∏ô‡πÑ‡∏ü‡∏•‡πå
            f.seek(0)
            print(f"\n‡∏´‡∏•‡∏±‡∏á seek(0): {f.tell()}")
            
            # seek(10) - ‡∏Å‡∏£‡∏∞‡πÇ‡∏î‡∏î‡πÑ‡∏õ‡∏ï‡∏≥‡πÅ‡∏´‡∏ô‡πà‡∏á 10
            f.seek(10)
            data = f.read(10)
            print(f"‡∏≠‡πà‡∏≤‡∏ô‡∏à‡∏≤‡∏Å‡∏ï‡∏≥‡πÅ‡∏´‡∏ô‡πà‡∏á 10: '{data}'")
            
            # seek(0, 2) - ‡∏Å‡∏£‡∏∞‡πÇ‡∏î‡∏î‡πÑ‡∏õ‡∏ó‡πâ‡∏≤‡∏¢‡πÑ‡∏ü‡∏•‡πå
            f.seek(0, 2)
            print(f"\n‡∏ï‡∏≥‡πÅ‡∏´‡∏ô‡πà‡∏á‡∏ó‡πâ‡∏≤‡∏¢‡πÑ‡∏ü‡∏•‡πå: {f.tell()} bytes")
            
    except Exception as e:
        print(f"‚ùå Error: {e}")


